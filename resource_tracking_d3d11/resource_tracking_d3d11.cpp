static ID3D11Device *device;
static IDXGISwapChain *swapchain;
static ID3D11DeviceContext *ctx;
static ID3D11Texture2D *textures[128];
static ID3D11ShaderResourceView *views[128];
static ID3D11RenderTargetView *rtv;

static HWND create_window(void)
{
    WNDCLASSW wc = {0};
    HWND ret;
    wc.lpfnWndProc = DefWindowProcW;
    wc.lpszClassName = L"resource_tracking_wc";
    RegisterClassW(&wc);

    ret = CreateWindowW(L"resource_tracking_wc", L"ddadda",
        WS_CAPTION, 100, 100, 640, 480, 0, 0, 0, 0);
    ShowWindow(ret, SW_SHOW);

    return ret;
}

static void create_device()
{
    D3D_FEATURE_LEVEL feature_level;
    UINT flags = 0;
    DXGI_SWAP_CHAIN_DESC swapchain_desc;
    HRESULT hr;
    ID3D11Texture2D *backbuffer;
    D3D11_VIEWPORT vp;

    feature_level = D3D_FEATURE_LEVEL_11_0;

    swapchain_desc.BufferDesc.Width = 640;
    swapchain_desc.BufferDesc.Height = 480;
    swapchain_desc.BufferDesc.RefreshRate.Numerator = 60;
    swapchain_desc.BufferDesc.RefreshRate.Denominator = 60;
    swapchain_desc.BufferDesc.Format = DXGI_FORMAT_R8G8B8A8_UNORM;
    swapchain_desc.BufferDesc.ScanlineOrdering = DXGI_MODE_SCANLINE_ORDER_UNSPECIFIED;
    swapchain_desc.BufferDesc.Scaling = DXGI_MODE_SCALING_UNSPECIFIED;
    swapchain_desc.SampleDesc.Count = 1;
    swapchain_desc.SampleDesc.Quality = 0;
    swapchain_desc.BufferUsage = DXGI_USAGE_RENDER_TARGET_OUTPUT;
    swapchain_desc.BufferCount = 1;
    swapchain_desc.OutputWindow = create_window();
    swapchain_desc.Windowed = TRUE;
    swapchain_desc.SwapEffect = DXGI_SWAP_EFFECT_DISCARD;
    swapchain_desc.Flags = 0;

    hr = D3D11CreateDeviceAndSwapChain(NULL, D3D_DRIVER_TYPE_HARDWARE, NULL, 0, NULL, 0, D3D11_SDK_VERSION,
        &swapchain_desc, &swapchain, &device, NULL, &ctx);
    if (hr != S_OK)
    {
        printf("bummer\n");
        exit(1);
    }

    hr = swapchain->GetBuffer(0, IID_ID3D11Texture2D, (void **)&backbuffer);
    if (hr != S_OK)
    {
        printf("bummer 4\n");
        exit(1);
    }

    hr = device->CreateRenderTargetView(backbuffer, NULL, &rtv);
    backbuffer->Release();
    if (hr != S_OK)
    {
        printf("bummer 5\n");
        exit(1);
    }
    ctx->OMSetRenderTargets(1, &rtv, NULL);

    vp.TopLeftX = 0;
    vp.TopLeftY = 0;
    vp.Width = 5;
    vp.Height = 5;
    vp.MinDepth = 0.0f;
    vp.MaxDepth = 1.0f;

    ctx->RSSetViewports(1, &vp);
}

static void create_resources(unsigned int tex_count)
{
    D3D11_TEXTURE2D_DESC texture2d_desc;
    DWORD stuff = 0x30303030;
    D3D11_SUBRESOURCE_DATA initdata = {&stuff, sizeof(stuff), sizeof(stuff)};
    unsigned int i;
    HRESULT hr;
    ID3D11Buffer *vb;
    D3D11_BUFFER_DESC buffer_desc;
    UINT stride, offset = 0;
    ID3D11VertexShader *vs;
    ID3D11PixelShader *ps;
    ID3D11InputLayout *input_layout;
    ID3D11SamplerState *sampler;
    D3D11_SAMPLER_DESC sampler_desc;
    static const unsigned int max_size = sizeof(textures) / sizeof(*textures);

    static const struct vec3 quad[] =
    {
        {-1.0f, -1.0f, 0.0f},
        {-1.0f,  1.0f, 0.0f},
        { 1.0f, -1.0f, 0.0f},
        { 1.0f,  1.0f, 0.0f},
    };
    static const D3D11_INPUT_ELEMENT_DESC layout_desc[] =
    {
        {"POSITION", 0, DXGI_FORMAT_R32G32B32_FLOAT, 0,  0, D3D11_INPUT_PER_VERTEX_DATA, 0},
    };

    if (tex_count > max_size)
    {
        printf("Use fewer than %lu textures please\n", max_size);
        exit(1);
    }

    for (i = 0; i < tex_count; ++i)
    {
        texture2d_desc.Width = 1;
        texture2d_desc.Height = 1;
        texture2d_desc.MipLevels = 1;
        texture2d_desc.ArraySize = 1;
        texture2d_desc.Format = DXGI_FORMAT_R8G8B8A8_UNORM;
        texture2d_desc.SampleDesc.Count = 1;
        texture2d_desc.SampleDesc.Quality = 0;
        texture2d_desc.Usage = D3D11_USAGE_DYNAMIC; // TODO: Find out if this has any impact on performance
        texture2d_desc.BindFlags = D3D11_BIND_SHADER_RESOURCE;
        texture2d_desc.CPUAccessFlags = D3D11_CPU_ACCESS_WRITE;
        texture2d_desc.MiscFlags = 0;
        hr = device->CreateTexture2D(&texture2d_desc, &initdata, &textures[i]);
        if (hr != S_OK)
        {
            printf("bummer2\n");
            exit(1);
        }

        hr = device->CreateShaderResourceView(textures[i], NULL, &views[i]);
        if (hr != S_OK)
        {
            printf("bummer3\n");
            exit(1);
        }

        ctx->PSSetShaderResources(i, 1, &views[i]);
    }

    buffer_desc.ByteWidth = sizeof(quad);
    buffer_desc.Usage = D3D11_USAGE_DEFAULT;
    buffer_desc.BindFlags = D3D11_BIND_VERTEX_BUFFER;
    buffer_desc.CPUAccessFlags = 0;
    buffer_desc.MiscFlags = 0;
    buffer_desc.StructureByteStride = 0;

    initdata.pSysMem = quad;
    initdata.SysMemPitch = 0;
    initdata.SysMemSlicePitch = 0;

    hr = device->CreateBuffer(&buffer_desc, &initdata, &vb);
    if (hr != S_OK)
    {
        printf("bummer 6\n");
        exit(1);
    }

    stride = sizeof(*quad);
    ctx->IASetVertexBuffers(0, 1, &vb, &stride, &offset);
    vb->Release();

    hr = device->CreateVertexShader(vs_code, sizeof(vs_code), NULL, &vs);
    if (hr != S_OK)
    {
        printf("bummer 7\n");
        exit(1);
    }

    hr = device->CreateInputLayout(layout_desc, sizeof(layout_desc) / sizeof(*layout_desc),
        vs_code, sizeof(vs_code), &input_layout);
    if (hr != S_OK)
    {
        printf("bummer 8\n");
        exit(1);
    }

    ctx->IASetInputLayout(input_layout);
    ctx->IASetPrimitiveTopology(D3D11_PRIMITIVE_TOPOLOGY_TRIANGLESTRIP);
    ctx->VSSetShader(vs, NULL, 0);

    hr = device->CreatePixelShader(ps_code, sizeof(ps_code), NULL, &ps);
    if (hr != S_OK)
    {
        printf("bummer 9\n");
        exit(1);
    }
    ctx->PSSetShader(ps, NULL, 0);

    sampler_desc.Filter = D3D11_FILTER_MIN_MAG_MIP_POINT;
    sampler_desc.AddressU = D3D11_TEXTURE_ADDRESS_CLAMP;
    sampler_desc.AddressV = D3D11_TEXTURE_ADDRESS_CLAMP;
    sampler_desc.AddressW = D3D11_TEXTURE_ADDRESS_CLAMP;
    sampler_desc.MipLODBias = 0.0f;
    sampler_desc.MaxAnisotropy = 0;
    sampler_desc.ComparisonFunc = D3D11_COMPARISON_NEVER;
    sampler_desc.BorderColor[0] = 0.0f;
    sampler_desc.BorderColor[1] = 0.0f;
    sampler_desc.BorderColor[2] = 0.0f;
    sampler_desc.BorderColor[3] = 0.0f;
    sampler_desc.MinLOD = 0.0f;
    sampler_desc.MaxLOD = 0.0f;
    device->CreateSamplerState(&sampler_desc, &sampler);
    ctx->PSSetSamplers(0, 1, &sampler);
}

static void draw()
{
    unsigned int i;
    /* Draw a decent number of times, otherwise the Present operation dominates the
     * time needed per frame. */
    for (i = 0; i < 1024; ++i)
        ctx->Draw(4, 0);
}

static void print_fps()
{
    static unsigned long frames;
    DWORD time = GetTickCount();
    static DWORD prev_time;

    frames++;
    /* every 1.5 seconds */
    if (time - prev_time > 1500)
    {
        printf("approx %.2ffps\n", 1000.0 * frames / (time - prev_time));
        prev_time = time;
        frames = 0;
    }
}

int main(int argc, char *argv[])
{
    unsigned int count = sizeof(textures) / sizeof(*textures);

    if (argc > 1)
        count = atoi(argv[1]);

    printf("using %u textures\n", count);
    create_device();
    create_resources(count);

    while (1)
    {
        draw();
        swapchain->Present(0, 0);
        print_fps();
    }
    return 0;
}
